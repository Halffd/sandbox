#include <stdio.h>

int main() {
    char c = 'X';  // Example character for variable 'c'
    char a = 'A';  // Example character for variable 'a'
    char b = 'B';  // Example character for variable 'b'

    printf("%c>%\n", c);    // Print character 'c' followed by '>%'
    printf("%c<%c\n", a, b); // Print character 'a' followed by '<' and character 'b'

    return 0;
}